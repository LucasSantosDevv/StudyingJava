package classe;

/* This pode ser usado para não precisar criar muitos nomes diferentes para variáveis, basicamente ele serva para
diferenciar variáveis entre si, como por exemplo: uma variável de instância para uma local */

public class This {

    String carro;
    int placa;
    int vMax;


    This(String carro, int placa, int vMax){
        this.carro = carro;
        this.placa = placa;
        this.vMax = vMax;
    }

}
